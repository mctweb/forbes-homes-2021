      <PropertyCardBasic :property="selected" />
    </Modal>
  </div>
</template>

<script>
import { stripeside } from '~/static/properties'

export default {
  props: {
    selectedplot: {
      type: String,
      default: null
    }
  },
  data () {
    return {
      classes: null,
      selected: null,
      openModal: false
    }
  },

  computed: {
    properties () {
      return stripeside.houses
    }
  },
  mounted () {
    this.addClasses()
  },
  methods: {
    addClasses () {
      const areas = document.querySelectorAll('[id*="Area"]')
      const houses = document.querySelectorAll('[id*="House"]')
      const garages = document.querySelectorAll('[id*="Garage"]')
      const plots = document.querySelectorAll('g[id*="Plot"]')

      for (let i = 0; i < areas.length; i++) {
        areas[i].classList.add('area')
      }
      for (let i = 0; i < houses.length; i++) {
        houses[i].classList.add('house')
      }
      for (let i = 0; i < garages.length; i++) {
        garages[i].classList.add('garage')
      }
      for (let i = 0; i < plots.length; i++) {
        plots[i].classList.add('plot')
      }
    },
    viewProperty (event) {
      const plot = event.currentTarget.id
      const property = stripeside.houses.find(x => 'plot ' + x.plot === plot.toLowerCase())
      this.selected = property
      this.openModal = true
    },
    getStatus (plot) {
      const property = stripeside.houses.find(x => x.plot === plot)
      let status = property.status
      if (this.selectedplot === plot) { status += ' Selected' }
      return status
    }

  }

}
</script>

